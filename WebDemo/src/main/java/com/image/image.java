package com.image;

public class image {
	String str;
	byte[] b;
	image(String str,byte[] b)
	{
		this.str=str;
		this.b=b;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	public byte[] getB() {
		return b;
	}
	public void setB(byte[] b) {
		this.b = b;
	}
	

}
